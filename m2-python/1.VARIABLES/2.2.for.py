'''
Estructuras control iterativo o de repeticion

bucle for es una estructura determinada
'''
names = ['juan', 'malala', 'rosa'] # plural porque es una estructura de datos

print('rosa' in names)

for name in names:
    print(name)

for numero in range(100):
    print(numero)

for numero in range(50, 100, 2):
    print(numero)
